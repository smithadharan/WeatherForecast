import Foundation
import UIKit

extension UILabel {
    
    func setTitleFont() {
        self.font = UIFont.systemFont(ofSize: 16)
    }
    
    func setSubTitleFont() {
        self.font = UIFont.systemFont(ofSize: 14)
    }
}
